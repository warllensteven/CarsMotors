    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try {
            String input = idDelete.getText().trim();
            if (input.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor ingrese un ID vÃ¡lido.");
                return;
            }

            int id = Integer.parseInt(input);

            int confirm = JOptionPane.showConfirmDialog(this,
                    "Â¿EstÃ¡s seguro de eliminar el servicio con ID " + id + "?",
                    "Confirmar eliminaciÃ³n",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                controller.deleteService(id); // <<< AquÃ­ se usa el controlador correcto
                JOptionPane.showMessageDialog(this, "Servicio eliminado correctamente.");
                idDelete.setText(""); // Limpiar campo
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un nÃºmero vÃ¡lido.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al eliminar el servicio.");
        }
    }//GEN-LAST:event_deleteActionPerformed

